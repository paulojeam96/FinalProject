							<p>“Estamos muito animados por apoiar oficialmente um dos títulos que mais cresce em todo cenário de e-Sports a tempo de participar das Grandes Finais de World of Tanks, em Varsóvia”, diz Min-Liag Tan, cofundador, CEO e diretor criativo da Razer. “A Wargaming é uma das desenvolvedoras de jogos mais prestigiadas do mundo, e o lançamento destes produtos feitos sob medida para um evento como esse é uma grande notícia para milhões de jogadores de World of Tanks.”</p>
							<br/>
